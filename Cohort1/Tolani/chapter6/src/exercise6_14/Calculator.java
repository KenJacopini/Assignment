package exercise6_14;


public class Calculator {
	
	
	public int calculateNthPower(int base, int exponent) {
		int result = Math.integerPower(base, exponent);
		return result;
	
	}
	
	public double calculateNthPower(double base, double exponent) {
		double result = Math.integerPower(base, exponent);
		return result;
	
	}
	
	public double calculateNthPower(double base, int exponent) {
		double result = Math.integerPower(base, exponent);
		return result;
	
	}

}
